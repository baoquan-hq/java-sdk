package com.baoquan.jsdk.comm;


public class EvidenceJudicialParam {

    private String anos;

    private String name;

    private String evidenceDesc;

    private Integer evidenceUseType;

    private String address;

    public String getAnos() {
        return anos;
    }

    public void setAnos(String anos) {
        this.anos = anos;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEvidenceDesc() {
        return evidenceDesc;
    }

    public void setEvidenceDesc(String evidenceDesc) {
        this.evidenceDesc = evidenceDesc;
    }

    public Integer getEvidenceUseType() {
        return evidenceUseType;
    }

    public void setEvidenceUseType(Integer evidenceUseType) {
        this.evidenceUseType = evidenceUseType;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
